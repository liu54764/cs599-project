def build_qa_prompt(query: str, context_documents: List[Dict[str, Any]], chat_history: List = None) -> str:
    """
    构建问答提示词，支持多轮对话

    Args:
        query: 用户问题
        context_documents: 检索到的上下文文档
        chat_history: 对话历史，格式为 [(question1, answer1), (question2, answer2)]
    Returns:
        完整的提示词
    """
    # 构建对话历史上下文
    history_str = ""
    if chat_history and len(chat_history) > 0:
        history_str = "\n### 对话历史\n"
        for i, (q, a) in enumerate(chat_history[-3:], 1):  # 只取最近3轮
            history_str += f"用户：{q}\n助手：{a}\n"
        history_str += "\n"

    # 构建文献上下文【删掉文献数字i，只显示文件名】
    context_str = ""
    for doc in context_documents:
        context_str += f"### 来源文档：{doc['file_name']}\n"
        context_str += f"内容: {doc['content']}\n\n"

    prompt = f"""
{history_str}以下是相关的文档内容：

{context_str}

用户问题：{query}

请基于上述文档内容回答问题。如果有对话历史，请考虑上下文连贯性。
"""

    return prompt